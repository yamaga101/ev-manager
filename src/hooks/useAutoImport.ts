export function useAutoImport() {
  const importRecords = useChargingStore((s) => s.importRecords);

  useEffect(() => {
    if (localStorage.getItem(IMPORT_FLAG)) return;
    const count = importRecords(SEED_RECORDS);
    localStorage.setItem(IMPORT_FLAG, String(Date.now()));
    if (count > 0) {
      console.log(`[AutoImport] ${count} charging records imported from photos`);
    }
  }, [importRecords]);
}
